// Helper function to find evaluation rules for a marker
export function findEvaluationRules(
  markerId: string,
  gender?: 'male' | 'female',
  age?: number
): EvaluationRule[] {
  return evaluationRules
    .filter((rule) => {
      if (rule.markerId !== markerId) return false;

      // Filter by gender if rule is gender-specific
      if (rule.gender && gender && rule.gender !== gender && rule.gender !== 'all') {
        return false;
      }

      // Filter by age if rule has age restrictions
      if (rule.ageMin !== null && age !== undefined && age < rule.ageMin) {
        return false;
      }
      if (rule.ageMax !== null && age !== undefined && age > rule.ageMax) {
        return false;
      }

      return true;
    })
    .sort((a, b) => a.displayOrder - b.displayOrder);
}

// Evaluate a value against rules
export function evaluateMarkerValue(
  markerId: string,
  value: number,
  gender?: 'male' | 'female',
  age?: number
): EvaluationRule | null {
  const rules = findEvaluationRules(markerId, gender, age);

  for (const rule of rules) {
    const matches = matchesRule(value, rule);
    if (matches) return rule;
  }

  return null;
}

function matchesRule(value: number, rule: EvaluationRule): boolean {
  switch (rule.comparison) {
    case 'lt':
      return value < (rule.valueMax ?? 0);
    case 'lte':
      return value <= (rule.valueMax ?? 0);
    case 'gt':
      return value > (rule.valueMin ?? 0);
    case 'gte':
      return value >= (rule.valueMin ?? 0);
    case 'between':
      return value >= (rule.valueMin ?? 0) && value <= (rule.valueMax ?? Infinity);
    default:
      return false;
  }
}
