#!/usr/bin/env python3
"""
Send the BFM Patient Eval Report PDF via email.
Uses SMTP with SSL. Reads credentials from project .env.local if available,
falls back to system environment variables.
"""

import smtplib
import ssl
import os
import sys
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from email.mime.application import MIMEApplication
from pathlib import Path
from datetime import datetime

# ── Config ──────────────────────────────────────────────────────────────────
TO_EMAIL    = "drrob@shslasvegas.com"
SUBJECT     = "patient output from co-pilot"
PDF_PATH    = "/Users/joncameron/Code/bfm-copilot/eval/bfm-eval-mar2026/BFM-Patient-Eval-Report-Mar2026.pdf"
PROJECT_ROOT = "/Users/joncameron/Code/bfm-copilot"

# ── Load .env.local for SMTP credentials ────────────────────────────────────
def load_env(path):
    env = {}
    try:
        with open(path) as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith("#") and "=" in line:
                    key, _, val = line.partition("=")
                    # Strip surrounding quotes
                    val = val.strip().strip('"').strip("'")
                    env[key.strip()] = val
    except FileNotFoundError:
        pass
    return env

# Try .env.local then .env
env = load_env(os.path.join(PROJECT_ROOT, ".env.local"))
if not env:
    env = load_env(os.path.join(PROJECT_ROOT, ".env"))

def get(key):
    return env.get(key) or os.environ.get(key, "")

SMTP_HOST = get("SMTP_HOST") or get("EMAIL_SERVER_HOST") or "smtp.gmail.com"
SMTP_PORT = int(get("SMTP_PORT") or get("EMAIL_SERVER_PORT") or "465")
SMTP_USER = get("SMTP_USER") or get("EMAIL_FROM") or get("EMAIL_SERVER_USER") or ""
SMTP_PASS = get("SMTP_PASS") or get("EMAIL_SERVER_PASSWORD") or ""
FROM_EMAIL = get("FROM_EMAIL") or SMTP_USER

# ── Validate ─────────────────────────────────────────────────────────────────
if not SMTP_USER or not SMTP_PASS:
    print("ERROR: SMTP credentials not found.")
    print("Please set SMTP_HOST, SMTP_PORT, SMTP_USER, SMTP_PASS in .env.local")
    print("")
    print("Found keys in .env.local:", list(env.keys()))
    sys.exit(1)

if not Path(PDF_PATH).exists():
    print(f"ERROR: PDF not found at {PDF_PATH}")
    sys.exit(1)

# ── Build email ───────────────────────────────────────────────────────────────
msg = MIMEMultipart()
msg["From"]    = FROM_EMAIL
msg["To"]      = TO_EMAIL
msg["Subject"] = SUBJECT
msg["Date"]    = datetime.now().strftime("%a, %d %b %Y %H:%M:%S +0000")

body_text = f"""Dr. Rob,

Please find attached the BFM Patient Evaluation Report generated by the BFM Copilot system.

This report covers a full clinical breakdown for 5 patients evaluated in March 2026:
  • Patient DH — Denis Hiestand (76M) — Urgency 5/5
  • Patient GD — Geraldine DeMartino (72F) — Urgency 5/5
  • Patient GG — Gregory Geiger (69M) — Urgency 5/5
  • Patient JW — Jennifer Weber (52F) — Urgency 2.5/5
  • Patient TH — Tim Haskins (53M) — Urgency 5/5

Each patient breakdown includes:
  — All deal breakers triggered (in priority order)
  — Complete frequency protocol list with sequencing
  — Full supplementation stack with dosing instructions
  — Five Levers assessment
  — Key clinical concerns
  — Monitoring recommendations

Report generated: {datetime.now().strftime("%B %d, %Y at %I:%M %p")}

—
BFM Copilot
"""

msg.attach(MIMEText(body_text, "plain"))

# Attach PDF
pdf_filename = Path(PDF_PATH).name
with open(PDF_PATH, "rb") as f:
    pdf_data = f.read()

attachment = MIMEApplication(pdf_data, _subtype="pdf")
attachment.add_header("Content-Disposition", "attachment", filename=pdf_filename)
msg.attach(attachment)

# ── Send ──────────────────────────────────────────────────────────────────────
print(f"Sending to:    {TO_EMAIL}")
print(f"Subject:       {SUBJECT}")
print(f"Attachment:    {pdf_filename} ({len(pdf_data):,} bytes)")
print(f"SMTP:          {SMTP_HOST}:{SMTP_PORT}")
print(f"From:          {FROM_EMAIL}")
print()

try:
    if SMTP_PORT == 465:
        # SSL from the start
        context = ssl.create_default_context()
        with smtplib.SMTP_SSL(SMTP_HOST, SMTP_PORT, context=context) as server:
            server.login(SMTP_USER, SMTP_PASS)
            server.sendmail(FROM_EMAIL, TO_EMAIL, msg.as_string())
    else:
        # STARTTLS
        context = ssl.create_default_context()
        with smtplib.SMTP(SMTP_HOST, SMTP_PORT) as server:
            server.ehlo()
            server.starttls(context=context)
            server.ehlo()
            server.login(SMTP_USER, SMTP_PASS)
            server.sendmail(FROM_EMAIL, TO_EMAIL, msg.as_string())

    print("✅  Email sent successfully!")

except smtplib.SMTPAuthenticationError as e:
    print(f"❌  SMTP Authentication failed: {e}")
    print("    Check SMTP_USER and SMTP_PASS in .env.local")
    sys.exit(1)
except smtplib.SMTPException as e:
    print(f"❌  SMTP error: {e}")
    sys.exit(1)
except OSError as e:
    print(f"❌  Connection error: {e}")
    sys.exit(1)
